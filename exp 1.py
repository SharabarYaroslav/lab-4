'''
Використовуючи поняття списку, напишіть програму, яка створює 3D
масив елементів a x b x c, кожен з яких має значення 0.
Значення a, b, c вводяться в одному рядку через пропуск.

Автор: Шарабар Ярослав Анатолійович
'''

# Введення розмірностей масиву
a, b, c = map(int, input("Введіть розміри масиву (a b c): ").split())

# Ініціалізація 3D масиву зі значеннями 0
three_d_array = [[[0 for _ in range(c)] for _ in range(b)] for _ in range(a)]

# Виведення створеного масиву
print("Створений 3D масив:")
for i in range(a):
    for j in range(b):
        for k in range(c):
            print(three_d_array[i][j][k], end=" ")
        print()
    print()
