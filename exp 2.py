'''
Напишіть програму, яка для двох послідовностей, що складаються з натуральних чисел,
що не перевершують n, буде визначати, які числа зустрічаються в кожній з послідовностей,
а які з чисел від 1 до n - в жодній з них. Спочатку на вхід програмі подається число n (1 ≤ n ≤ 255).
У другому рядку вхідних даних знаходяться елементи першої послідовності, у третьому рядку -
елементи другої послідовності. Виведіть у першому рядку в порядку зростання без повторень
числа, які зустрічаються в кожній з послідовностей, а у другому рядку - в порядку зростання
числа від 1 до 255, які не зустрічаються в жодній з них.

Автор: Шарабар Ярослав Анатолійович
'''
# Зчитування вхідних даних
n = int(input("Введіть число n (1 ≤ n ≤ 255): "))
seq1 = set(map(int, input("Введіть елементи першої послідовності: ").split()))
seq2 = set(map(int, input("Введіть елементи другої послідовності: ").split()))

# Знаходження спільних елементів
common_elements = sorted(seq1 & seq2)

# Знаходження відсутніх елементів
all_elements = set(range(1, n + 1))
missing_elements = sorted(all_elements - (seq1 | seq2))

# Виведення результатів
print("Спільні елементи:", common_elements)
print("Відсутні елементи:", missing_elements)

